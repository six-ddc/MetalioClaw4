// ---------------------------------------------------------------------------
// ThemeScreen
//
// 主题选择 App。布局（720x720）：
//
//   ┌────────────────────────────────────────────────────────┐
//   │  [←]    主题                                            │  ← header 90px
//   ├────────────────────────────────────────────────────────┤
//   │                                                        │
//   │      ┌──────┐         ┌──────┐                         │
//   │      │ T1   │         │ T2   │  ← 180x180 预览卡片      │
//   │      └──────┘         └──────┘                         │
//   │      主题 1            主题 2                           │
//   │                                                        │
//   │      ┌──────┐         ┌──────┐                         │
//   │      │ T3   │         │ T4   │                         │
//   │      └──────┘         └──────┘                         │
//   │      主题 3            主题 4                           │
//   │                                                        │
//   │  切换主题后将自动重启设备以应用新主题                       │
//   └────────────────────────────────────────────────────────┘
//
// 交互：
//   - 点击非当前主题 -> 弹模态对话框 "切换并重启 / 取消"
//   - 点击当前主题 -> no-op
//   - 确认切换 -> ThemeManager::SetCurrentThemeId() 写 NVS，
//     Application::Reboot() 重启设备，下次开机即用新主题加载图标
// ---------------------------------------------------------------------------
class ThemeScreen {
public:
    static lv_obj_t* Create();
    static void LifecycleCallback(screen_lifecycle_event_t event);
};
